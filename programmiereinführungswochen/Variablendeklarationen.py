# Variablendeklarationen 
# Deklaration der Variablen anzahl vom Typ int mit Wertzuweisung 
anzahl = 12 
print(anzahl) 
# Deklaration der Variablen preis vom Typ float mit Wertzuweisung 
preis = 12.50 
print(preis) 
# Deklaration der Variablen vorname vom Typ str mit Wertzuweisung 
vorname = "Ben" 
print(vorname) 
print(vorname, "kauft", anzahl, "Artikel", "zum Einzelpreis von ", preis, "Euro") 
print(vorname, "bezahlt", anzahl * preis, "Euro")